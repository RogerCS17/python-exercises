"""Extrae un elemento específico de una tupla"""

# Se define una tupla
array_tuple = (1, 2, 3, 4, 5)
# Se muestra la tupla
print(f"Tupla: {array_tuple} y es de tipo: {type(array_tuple)}")

# Al igual que las lista, se puede usar la indexación para extraer elementos
# Se muestra el resultado
print(f"El primer elemento de la tupla es: {array_tuple[0]}")
