"""Realiza operaciones básicas con conjuntos unión e intersección"""

# Se define dos conjuntos
set01 = {1, 2, 3, 4, 5}
set02 = {3, 4, 5, 6, 7}

# Se muestra los conjuntos
print(f"Conjunto 1: {set01}")
print(f"Conjunto 2: {set02}")

# Se realiza las operaciones básicas
# Se muestra el resultado de la unión
print(f"Unión: {set01.union(set02)}")

# Se muestra el resultado de la intersección
print(f"Unión: {set01.intersection(set02)}")
